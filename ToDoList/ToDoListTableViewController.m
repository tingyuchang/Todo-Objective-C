#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    [tableView deselectRowAtIndexPath:indexPath animated:NO];
    
    // the finished item
    NSString* finishedItem = [[TodoList defaultList].items objectAtIndex:indexPath.row];
    
    // remove from active
    [[TodoList defaultList].items removeObject:finishedItem];
    // add to finished
    [[TodoList defaultList].finsihedItems addObject:finishedItem];
    
    [tableView deleteRowsAtIndexPaths:[NSArray arrayWithObjects:indexPath, nil]
                     withRowAnimation:UITableViewRowAnimationRight];
    [[TodoList defaultList] save];
}

